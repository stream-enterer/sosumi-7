/// Input key identifiers for keyboard and mouse.
#[derive(Copy, Clone, Debug, PartialEq, Eq, Hash)]
pub enum InputKey {
    // Mouse buttons
    MouseLeft,
    MouseRight,
    MouseMiddle,
    MouseX1,
    MouseX2,

    // Modifier keys
    Shift,
    Ctrl,
    Alt,
    Meta,

    // Navigation
    Escape,
    Tab,
    Enter,
    Backspace,
    Delete,
    Insert,
    Home,
    End,
    PageUp,
    PageDown,
    ArrowUp,
    ArrowDown,
    ArrowLeft,
    ArrowRight,

    // Function keys
    F1,
    F2,
    F3,
    F4,
    F5,
    F6,
    F7,
    F8,
    F9,
    F10,
    F11,
    F12,

    // Mouse wheel
    WheelUp,
    WheelDown,
    WheelLeft,
    WheelRight,

    // Printable keys
    Space,
    Key(char),
}

/// Variant of an input event.
#[derive(Copy, Clone, Debug, PartialEq, Eq, Hash)]
pub enum InputVariant {
    /// Key or button pressed down.
    Press,
    /// Key or button released.
    Release,
    /// Repeated key press (held down).
    Repeat,
    /// Mouse moved (no button state change).
    Move,
}

/// An input event.
#[derive(Clone, Debug)]
pub struct InputEvent {
    /// Which key or button.
    pub key: InputKey,
    /// Event variant (press, release, repeat).
    pub variant: InputVariant,
    /// Characters generated by this event (for text input).
    pub chars: String,
    /// Whether this is a repeated event from holding a key.
    pub is_repeat: bool,
    /// Mouse X position in panel coordinates.
    pub mouse_x: f64,
    /// Mouse Y position in panel coordinates.
    pub mouse_y: f64,
}

impl InputEvent {
    pub fn press(key: InputKey) -> Self {
        Self {
            key,
            variant: InputVariant::Press,
            chars: String::new(),
            is_repeat: false,
            mouse_x: 0.0,
            mouse_y: 0.0,
        }
    }

    pub fn release(key: InputKey) -> Self {
        Self {
            key,
            variant: InputVariant::Release,
            chars: String::new(),
            is_repeat: false,
            mouse_x: 0.0,
            mouse_y: 0.0,
        }
    }

    pub fn with_chars(mut self, chars: &str) -> Self {
        self.chars = chars.to_string();
        self
    }

    pub fn with_mouse(mut self, x: f64, y: f64) -> Self {
        self.mouse_x = x;
        self.mouse_y = y;
        self
    }
}
